package Feb22;

public class ArithmeticDemo {
    public float sum(float x,float y) {
        return x + y;
    }
    public float sum(float x,float y, float z) {
        return x + y + z;
    }
    public float sum(float x,float y, float z, float a) {
        return x + y + z + a;
    }
    public float sub(float x,float y) {
        return  x - y;
    }
    public float mul(float x,float y) {
        return x * y;
    }
    public float divi(float x,float y) {
        return  x / y;
    }
    public float mod(float x,float y) {
        return x % y;
    }


}
